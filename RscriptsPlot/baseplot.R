setwd("projects/qiaocun")
tiles<-read.table("tiles.csv",header=T,sep=",")
cover<-subset(tiles,tiles$type=="cover")
pan<-subset(tiles,tiles$type=="pan")
library(ggplot2)
p1<-ggplot(cover,aes(width,stat(density)))+geom_histogram(bins=25,fill="#FC4E07",color="black",alpha=0.6)+geom_density(color="blue")+scale_x_continuous(limits=c(7,31))
p2<-ggplot(pan,aes(width,stat(density)))+geom_histogram(bins=25,fill="#00AFBB",color="black",alpha=0.6)+geom_density(color="red")+scale_x_continuous(limits=c(7,31))
p3<-ggplot(cover,aes(height,stat(density)))+geom_histogram(bins=25,fill="#FC4E07",color="black",alpha=0.6)+geom_density(color="blue")+scale_x_continuous(limits=c(2,9))
p4<-ggplot(pan,aes(height,stat(density)))+geom_histogram(bins=25,fill="#00AFBB",color="black",alpha=0.6)+geom_density(color="red")+scale_x_continuous(limits=c(2,9))
p5<-ggplot(cover,aes(thickness,stat(density)))+geom_histogram(bins=25,fill="#FC4E07",color="black",alpha=0.6)+geom_density(color="blue")+scale_x_continuous(limits=c(0.4,1.7))
p6<-ggplot(pan,aes(thickness,stat(density)))+geom_histogram(bins=25,fill="#00AFBB",color="black",alpha=0.6)+geom_density(color="red")+scale_x_continuous(limits=c(0.4,1.7))
library(patchwork)
p1+p3+p5+p2+p4+p6+plot_layout(nrow=2)
